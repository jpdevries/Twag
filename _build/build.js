tags.map(function(tag) {
  const file = fs.readFileSync(__dirname + `/attributes/${tag}.json`,'utf8'),
  json = JSON.parse(file),
  isVoidElement = voids.includes(tag),
  attributes = json.attributes.map(function(attribute) {
    return `{% if ${attribute} %} ${attribute}="{{${attribute}}}"{% endif %}`;
  });

  let s = `<${tag}${attributes.join('')}>`;
  if(!isVoidElement) s += `
  {% block innerHTML %}{% endblock %}
</${tag}>`;

  fs.writeFileSync(__dirname + `/../twag/${tag}.twig`, s, 'utf8');
});
